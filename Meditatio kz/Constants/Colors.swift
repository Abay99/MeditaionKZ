import Foundation
import UIKit

class Color {

    class func main() -> UIColor {
        return #colorLiteral(red: 0.1921568627, green: 0.2, blue: 0.3333333333, alpha: 1)
    }

    class func lightMain() -> UIColor {
        return #colorLiteral(red: 0.1921568627, green: 0.2, blue: 0.3333333333, alpha: 0.76)
    }

    class func veryLightMain() -> UIColor {
        return #colorLiteral(red: 0.1921568627, green: 0.2, blue: 0.3333333333, alpha: 0.400332355)
    }

    class func black() -> UIColor {
        return #colorLiteral(red: 0.1176470588, green: 0.1176470588, blue: 0.1176470588, alpha: 1)
    }

    class func lightGray() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.968627451, blue: 0.968627451, alpha: 1)
    }

    class func gray() -> UIColor {
        return #colorLiteral(red: 0.6549019608, green: 0.6549019608, blue: 0.6549019608, alpha: 1)
    }

    class func darkGray() -> UIColor {
        return #colorLiteral(red: 0.3803921569, green: 0.3803921569, blue: 0.3803921569, alpha: 1)
    }

    class func white() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }

    class func orange() -> UIColor {
        return #colorLiteral(red: 0.9176470588, green: 0.5294117647, blue: 0.2941176471, alpha: 1)
    }

    class func beige() -> UIColor {
        return #colorLiteral(red: 0.9294117647, green: 0.9058823529, blue: 0.9058823529, alpha: 1)
    }
} 
